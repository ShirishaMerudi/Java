package ControlFlowStatements;

public class LoopsConcept {

	public static void main(String[] args) 
	{
		for (int i = 1; i<=5; i++)
		{
			System.out.print("Loops  ");
		}
		System.out.println();

		//Print 1 to 10
		for (int i=1;i<=10 ;i++)
		{
			System.out.print(i+ "  ");
		}
		System.out.println();

	}

}
